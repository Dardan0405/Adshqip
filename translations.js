// Store original English text for reverting
const originalTexts = new Map();

let currentLang = localStorage.getItem('adshqip-lang') || 'en';

function getAllTextNodes(root) {
  const walker = document.createTreeWalker(
    root,
    NodeFilter.SHOW_TEXT,
    {
      acceptNode: function (node) {
        // Skip script, style, pre, code elements
        const parent = node.parentElement;
        if (!parent) return NodeFilter.FILTER_REJECT;
        const tag = parent.tagName.toLowerCase();
        if (['script', 'style', 'pre', 'code', 'noscript', 'textarea'].includes(tag)) {
          return NodeFilter.FILTER_REJECT;
        }
        // Only accept nodes with meaningful text
        if (node.textContent.trim().length > 1) {
          return NodeFilter.FILTER_ACCEPT;
        }
        return NodeFilter.FILTER_REJECT;
      }
    }
  );

  const nodes = [];
  while (walker.nextNode()) {
    nodes.push(walker.currentNode);
  }
  return nodes;
}

// Store original placeholders for reverting
const originalPlaceholders = new Map();

function translatePage(lang) {
  const dict = textTranslations[lang];
  if (!dict) return;

  const textNodes = getAllTextNodes(document.body);

  textNodes.forEach(node => {
    const trimmed = node.textContent.trim();

    // Store original text if not already stored
    if (!originalTexts.has(node)) {
      originalTexts.set(node, node.textContent);
    }

    // Check for exact match
    if (dict[trimmed]) {
      const leading = node.textContent.match(/^\s*/)[0];
      const trailing = node.textContent.match(/\s*$/)[0];
      node.textContent = leading + dict[trimmed] + trailing;
      return;
    }

    // Check for partial matches within longer text (for paragraphs etc.)
    let newText = node.textContent;
    let changed = false;
    // Sort keys by length descending to match longer strings first
    const sortedKeys = Object.keys(dict).sort((a, b) => b.length - a.length);
    for (const key of sortedKeys) {
      if (key.length > 3 && newText.includes(key)) {
        newText = newText.replace(key, dict[key]);
        changed = true;
      }
    }
    if (changed) {
      node.textContent = newText;
    }
  });

  // Translate placeholder attributes on input elements
  document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(el => {
    const ph = el.getAttribute('placeholder');
    if (ph && dict[ph]) {
      if (!originalPlaceholders.has(el)) {
        originalPlaceholders.set(el, ph);
      }
      el.setAttribute('placeholder', dict[ph]);
    }
  });

  // Translate title attributes (tooltips)
  document.querySelectorAll('[title]').forEach(el => {
    const title = el.getAttribute('title');
    if (title && dict[title]) {
      if (!originalPlaceholders.has(el)) {
        originalPlaceholders.set(el, { attr: 'title', value: title });
      }
      el.setAttribute('title', dict[title]);
    }
  });
}

function revertPage() {
  originalTexts.forEach((originalText, node) => {
    if (node.parentElement) { // Check node is still in DOM
      node.textContent = originalText;
    }
  });

  // Revert placeholders and title attributes
  originalPlaceholders.forEach((original, el) => {
    if (el.parentElement || el.isConnected) {
      if (typeof original === 'string') {
        el.setAttribute('placeholder', original);
      } else if (original && original.attr) {
        el.setAttribute(original.attr, original.value);
      }
    }
  });
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('adshqip-lang', lang);

  if (lang === 'en') {
    revertPage();
  } else {
    // First revert to English, then apply target language
    revertPage();
    translatePage(lang);
  }

  updateLanguageSelectors(lang);
}

function updateLanguageSelectors(lang) {
  const code = lang.toUpperCase();

  // Update the fixed language selector button text
  const langBtn = document.querySelector('#languageSelector .group > button span');
  if (langBtn) langBtn.textContent = code;

  // Update active states in fixed language selector
  document.querySelectorAll('[data-lang]').forEach(link => {
    const linkLang = link.getAttribute('data-lang');
    const badge = link.querySelector('span');
    if (badge) {
      if (linkLang === lang) {
        badge.className = 'flex items-center justify-center w-6 h-6 rounded-full bg-brand-600 text-xs font-medium text-white';
      } else {
        badge.className = 'flex items-center justify-center w-6 h-6 rounded-full bg-brand-600/10 text-xs font-medium text-brand-600';
      }
    }
  });

  // Update footer language selector
  document.querySelectorAll('[data-lang-footer]').forEach(link => {
    const linkLang = link.getAttribute('data-lang-footer');
    const badge = link.querySelector('span');
    if (badge) {
      if (linkLang === lang) {
        badge.className = 'flex items-center justify-center w-5 h-5 rounded-full bg-brand-600 text-[10px] font-medium text-white';
      } else {
        badge.className = 'flex items-center justify-center w-5 h-5 rounded-full bg-brand-600/10 text-[10px] font-medium text-brand-600';
      }
    }
  });
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
  // Set up language selector click handlers (fixed sidebar)
  document.querySelectorAll('[data-lang]').forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      setLanguage(link.getAttribute('data-lang'));
    });
  });

  // Set up footer language selector click handlers
  document.querySelectorAll('[data-lang-footer]').forEach(link => {
    link.addEventListener('click', (e) => {
      e.preventDefault();
      setLanguage(link.getAttribute('data-lang-footer'));
    });
  });

  // Apply saved language on page load
  if (currentLang !== 'en') {
    translatePage(currentLang);
  }
  updateLanguageSelectors(currentLang);
});
